export const colorCode = (color) => {
	let color_id = COLORS.indexOf(color);
	return color_id;
};

export const COLORS = [
      'black',
      'brown',
      'red',
      'orange',
      'yellow',
      'green',
      'blue',
      'violet',
      'grey',
      'white',
    ];
